    private void proveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proveButtonActionPerformed
        boolean result = false;
        if (textarea.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "The input data is empty", "ERROR!", JOptionPane.DEFAULT_OPTION);
            return;
        }
        
        Parser p = new Parser();
        try {
            if (allRules)
                p.addAllRules();  
            else {
                if (compRules)
                    p.addRules("rules/_Comparison.txt");  
                if (arithRules)
                    p.addRules("rules/_Arithmetic.txt");
                if (zeroRules)
                    p.addRules("rules/_ZeroNonzero.txt");
            }
        } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage() , "FAULT!", JOptionPane.DEFAULT_OPTION);
                return;
        }
        if (addMannual) {
            for (String f:addManNms)
                try {
                    p.addRules(f);
                } catch (IOException exe) {
                    JOptionPane.showMessageDialog(this, exe.getMessage() , "FAULT!", JOptionPane.DEFAULT_OPTION);
                    addManNms.remove(f);
                    return;
                }
            }
            
        try {
            result = p.prove_win(textarea.getText());
        } catch (IOException | PredicateParseException | PredicateCreateException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage() , "FAULT!", JOptionPane.DEFAULT_OPTION);
            return;
        }
        JOptionPane.showMessageDialog(this, "The result is " + result , "SUCCESS!", JOptionPane.DEFAULT_OPTION);
    }//GEN-LAST:event_proveButtonActionPerformed

