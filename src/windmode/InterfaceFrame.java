    private void SaveMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveMenuItemActionPerformed
        JFileChooser chooser = new JFileChooser();
        int retrival = chooser.showSaveDialog(null);
        if (retrival == JFileChooser.APPROVE_OPTION) {
            try {
                BufferedWriter bw = new BufferedWriter(new FileWriter(chooser.getSelectedFile().getAbsolutePath()));
                PrintWriter pw = new PrintWriter(chooser.getSelectedFile().getAbsolutePath());
                FileWriter fw = new FileWriter(chooser.getSelectedFile().getAbsolutePath());
                String[] parts = textarea.getText().split("\n");
                for (String s: parts)
                    bw.write(s);
                bw.flush();
                bw.close();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage() , "FAULT!", JOptionPane.DEFAULT_OPTION);

            }
            
        }
    }//GEN-LAST:event_SaveMenuItemActionPerformed

